package com.prmncr.normativecontrol.dtos.documentparams;

public class PageMargin {
	public int top;
	public int right;
	public int bottom;
	public int left;

	public PageMargin(int top, int right, int bottom, int left) {
	    this.top = top;
	    this.right = right;
	    this.bottom = bottom;
	    this.left = left;
	}
}
